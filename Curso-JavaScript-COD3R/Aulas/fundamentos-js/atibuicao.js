a = 7;
b = 3;

// Atibuições

a += b; // a = a + b

console.log(a);

b += a; // b = b + a (que está valendo 10 por causa da atribuição anterior)

console.log(b);

a -= 4; // a = a - 4

console.log(a);

b -= 4; // b = b - 4

console.log(b);

a *= 2; // a = a * 2 (que está valendo 6 por causa da atribuição de subtração anterior)

console.log(a);

b *= 2; // b = b * 2 (que está valendo 9 por causa da atribuição de subtração anterior)

console.log(b);

a /= 2; // a = a / 2

console.log(a);

b /= 2; // b = b / 2

console.log(b);

a %= 2; // a = a % 2 (módulo ou resto da divisão de a por 2)

console.log(a);

b %= 2; // b = b % 2 (módulo ou resto da divisão de b por 2)

console.log(b);



